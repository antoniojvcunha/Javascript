// operadores aritmeticos

// +   -   *   /   %


const a = 7;
const b = 3;

const soma = a + b; // o simbolo + serve 2 propositos:operador de soma aritmetica e operador de concatenaçao; ou seja, se a variavel a fosse "7" e o b fosse na mesma 3, o resultado seria uma string "73", pois havendo uma string numa soma, automaticamente é convertido o valor numerico em string
const sub = a - b;
const multi = a * b;
const divi = a / b;
const resto = a % b;

console.log(soma);
console.log(sub);
console.log(multi);
console.log(divi);
console.log(resto);
